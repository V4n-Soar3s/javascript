//Arrays também são indexados, porém de forma diferente das strings cada valor dentro do array possui um número, eles nao são indexados pelo seu valor, mas sim por elemento, vide exemplo:

//               0       1        2
const alunos = ['Luiz', 'Maria', 'João'];
console.log(alunos);//exibir o array completo 
console.log(alunos[0]);//se eu quiser acessar um desses indices por inteiro eu informo o nome da minha constante e entre chaves informo o indice do elemento que estou chamando 
console.log(alunos[2]);// vai ser apresentado o nome João que se encontra no indice 2 
