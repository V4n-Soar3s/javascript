//Também podemos remover elementos do array utilizando pop, vide exemplo: 
//               0       1        2
const alunos = ['Luiz', 'Maria', 'João'];

const removido = alunos.pop();//estou removendo o elemento do final do array, nesse caso foi tirado o João e podemos salvar o elemento que estamos removendo do array em uma variavel.
console.log(removido);//aqui vai apresentar quem foi removido
console.log(alunos);// aqui aparece a lista sem a pessoa que foi retirada