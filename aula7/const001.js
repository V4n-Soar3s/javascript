// Constantes - Aula 07 (Variável constante é uma variável que nao muda o seu valor)

/*
 * Não podemos criar constantes com palavras reservadas
 * Constantes precisam ter nomes significativos
 * Não podemos começar o nome de uma constante com um nº
 * Não podem conter espaços ou traços
 * Utilizamos camelCase
 * São case-sensitive
 * Não podemos modificar o valor de uma constante
 * Não podemos apenas declarar uma constante, precisamos declarar e definir ela. 
 * NÃO UTILIZE VAR, UTILIZE CONST.
 */

const nome = 'Vanessa';
console.log(nome);
